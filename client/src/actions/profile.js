export const deleteAccount = () => async (dispatch) => {
  if (window.confirm("Jeste li sigurni? Ovo se ne može vratiti!")) {
    try {
      await axios.delete("/api/profile");
      dispatch({ type: CLEAR_PROFILE });
      dispatch({ type: ACCOUNT_DELETED });
      dispatch(setAlert("Vaš račun je trajno izbrisan."));
    } catch (err) {
      dispatch({
        type: PROFILE_ERROR,
        payload: { msg: err.response.statusText, status: err.response.status },
      });
    }
  }
};

export const uploadProfilePicture = (picture) => async (dispatch) => {
  let type = picture.picture.split(".").pop();
  try {
    if (type === "jpg" || type === "jpeg" || type === "png" || type === "gif") {
      const config = {
        headers: {
          "Content-Type": "application/json",
        },
      };
      const res = await axios.post("/api/profile/avatar", picture, config);
      dispatch({
        type: UPDATE_PROFILE,
        payload: res.data,
      });
      dispatch(setAlert("Profilna slika je osvježena.", "success"));
    } else {
      dispatch(setAlert("Nije odgovarajući format.", "danger"));
    }
  } catch (err) {
    const errors = err.response.data.errors;
    if (errors) {
      errors.forEach((error) => dispatch(setAlert(error.msg, "danger")));
    }
    dispatch({
      type: PROFILE_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

